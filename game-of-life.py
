class Solution:
    def gameOfLife(self, board: list[list[int]]) -> None:
        m = len(board)
        n = len(board[0])

        def count_live_neighbors(r, c):
            count = 0
            for i in range(max(0, r - 1), min(m, r + 2)):
                for j in range(max(0, c - 1), min(n, c + 2)):
                    if (i != r or j != c) and board[i][j] in (1, 2):
                        count += 1
            return count

        for r in range(m):
            for c in range(n):
                live_neighbors = count_live_neighbors(r, c)

                if board[r][c] == 1:
                    if live_neighbors < 2 or live_neighbors > 3:
                        board[r][c] = 2  # Mark as dead in the next state
                else:
                    if live_neighbors == 3:
                        board[r][c] = -1 # Mark as live in the next state

        for r in range(m):
            for c in range(n):
                if board[r][c] == 2:
                    board[r][c] = 0
                elif board[r][c] == -1:
                    board[r][c] = 1
