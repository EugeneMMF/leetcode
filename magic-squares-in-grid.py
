
class Solution:
    def numMagicSquaresInside(self, grid: list[list[int]]) -> int:
        R, C = len(grid), len(grid[0])
        
        def is_magic(r, c):
            if grid[r+1][c+1] != 5:
                return False

            nums = []
            for i in range(3):
                for j in range(3):
                    nums.append(grid[r+i][c+j])
            
            seen = set()
            for num in nums:
                if not (1 <= num <= 9):
                    return False
                seen.add(num)
            
            if len(seen) != 9:
                return False
            
            target_sum = 15
            
            if sum(grid[r+0][c:c+3]) != target_sum: return False
            if sum(grid[r+1][c:c+3]) != target_sum: return False
            if sum(grid[r+2][c:c+3]) != target_sum: return False
            
            if grid[r+0][c+0] + grid[r+1][c+0] + grid[r+2][c+0] != target_sum: return False
            if grid[r+0][c+1] + grid[r+1][c+1] + grid[r+2][c+1] != target_sum: return False
            if grid[r+0][c+2] + grid[r+1][c+2] + grid[r+2][c+2] != target_sum: return False
            
            if grid[r+0][c+0] + grid[r+1][c+1] + grid[r+2][c+2] != target_sum: return False
            if grid[r+0][c+2] + grid[r+1][c+1] + grid[r+2][c+0] != target_sum: return False
            
            return True

        count = 0
        for r in range(R - 2):
            for c in range(C - 2):
                if is_magic(r, c):
                    count += 1
        
        return count
